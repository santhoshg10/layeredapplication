package com.santhosh.dto;

public class BatsmanDTO
{
	private int batsmanId;
	private String name;
	private int age;
	private int runsScored;
	private int noOfInnings;
	private int notOuts;
	public int getBatsmanId() {
		return batsmanId;
	}
	public void setBatsmanId(int batsmanId) {
		this.batsmanId = batsmanId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getRunsScored() {
		return runsScored;
	}
	public void setRunsScored(int runsScored) {
		this.runsScored = runsScored;
	}
	public int getNoOfInnings() {
		return noOfInnings;
	}
	public void setNoOfInnings(int noOfInnings) {
		this.noOfInnings = noOfInnings;
	}
	public int getNotOuts() {
		return notOuts;
	}
	public void setNotOuts(int notOuts) {
		this.notOuts = notOuts;
	}
	
}
